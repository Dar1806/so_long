#include "../includes/libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	w;

	i = 0;
	w = 0;
	while (s[i])
	{
		w = write(fd, &s[i], 1);
		i++;
	}
	return (w);
}
