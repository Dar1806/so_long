#include "../includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	ret;

	if (n == -2147483648)
	{
		ret = write(fd, "-2147483648", 11);
		(void)ret;
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
		ft_putchar_fd(n + 48, fd);
}
